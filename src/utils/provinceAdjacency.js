// 获取两个省份是否相邻
export function areProvincesAdjacent(province1, province2) {
  const neighbors = provinceAdjacencyMap[province1];
  return neighbors ? neighbors.includes(province2) : false;
}

// 获取省份的所有相邻省份
export function getAdjacentProvinces(province) {
  return provinceAdjacencyMap[province] || [];
}

// 构建完整的相邻关系图（返回Map格式，便于使用）
export function buildAdjacencyGraph() {
  const graph = new Map();
  
  Object.keys(provinceAdjacencyMap).forEach((province) => {
    graph.set(province, new Set(provinceAdjacencyMap[province]));
  });
  
  return graph;
}

